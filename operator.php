<?php
// Operator (Aritmatika & Komparasi)

// Operator Aritmatika
// +, -, *, /, %
$x = 10;
$y = 10;

echo "Operator Aritmatika";
echo "<br> Hasil Tambah $x + $y = ".$x + $y;
echo "<br> Hasil Kurang $x - $y = ".$x - $y;
echo "<br> Hasil Tambah $x * $y = ".$x * $y;
echo "<br> Hasil Tambah $x / $y = ".$x / $y;
echo "<br> Hasil Tambah $x % $y = ".$x % $y;

// Operator Komparasi
// ==, !=, >, <, >=, <=
echo "<br> <br> Operator Komparasi <br>";
echo var_dump($x > $y);
echo "<br>";
echo var_dump($x == $y);
?>